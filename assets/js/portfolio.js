var a = window.document.getElementById('specialization1')
function hover(){
    a.style.borderStyle = 'solid'
    a.style.borderWidth = '2px'
    a.style.borderColor = 'blue'
    a.style.transition = '0.1 ease-in'
}

function out_hover(){
    a.style.borderStyle = 'none'
}


